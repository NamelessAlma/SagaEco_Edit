    {
        public P10001034()
        {
            this.EventID = 10001034;
        }

        public override void OnEvent(ActorPC pc)
        {
            BitMask<Knights> Knights_mask = pc.CMask["Knights"];

            if (pc.Skills2.ContainsKey(706) || pc.SkillsReserve.ContainsKey(706))
            {
                switch (Select(pc, "這是往摩根市的飛空庭喔", "", "坐普通席", "坐頭等席", "暫時不用"))
                {
                    case 1:
                        Knights_mask.SetValue(Knights.出示西國許可證, false);
                        PlaySound(pc, 2426, false, 100, 50);
                        Say(pc, 131, "向摩根市出發！$R;");
                        PlaySound(pc, 2438, false, 100, 50);
                        ShowEffect(pc, 94, 215, 8066);
                        Wait(pc, 2000);
                        Warp(pc, 10060000, 228, 175);
                        break;
                    case 2:
                        Knights_mask.SetValue(Knights.出示西國許可證, false);
                        Say(pc, 131, "希望您會有一個愉快的旅程唷$R;");
                        PlaySound(pc, 2426, false, 100, 50);
                        Say(pc, 131, "向摩根市出發$R;");
                        PlaySound(pc, 2438, false, 100, 50);
                        ShowEffect(pc, 94, 215, 8066);
                        Wait(pc, 2000);
                        Warp(pc, 10060000, 228, 185);
                        break;
                    case 3:
                        break;
                }
                return;
            }
            if (pc.Account.GMLevel >= 100 || Knights_mask.Test(Knights.西國過境檢查完成) || Knights_mask.Test(Knights.出示西國許可證))
            {
                switch (Select(pc, "這是往摩根市的飛空庭喔", "", "搭乘飛空庭", "暫時不用"))
                {
                    case 1:
                        Knights_mask.SetValue(Knights.出示西國許可證, false);
                        PlaySound(pc, 2426, false, 100, 50);
                        Say(pc, 131, "向摩根市出發！$R;");
                        PlaySound(pc, 2438, false, 100, 50);
                        ShowEffect(pc, 94, 215, 8066);
                        Wait(pc, 2000);
                        Warp(pc, 10060000, 228, 195);
                        break;
                    case 2:
                        break;
                }
                return;
            }
            Say(pc, 131, "請辦理搭乘手續喔$R;");
        }
    }
